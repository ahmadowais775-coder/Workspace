#!/usr/bin/env python3
"""
Generate a RESULTS document in standard review-paper / journal format:

  HPTLC TLC, 2D and 3D Fingerprint Analysis of the Ethyl Acetate Fraction of the
  Methanol Extract of Sphagneticola trilobata (Wedelia trilobata) in Comparison
  with the Standard QUERCETIN. (Rutin has been removed from this study.)

Formatting: A4 page, standard 1-inch (2.54 cm) margins, Times New Roman body,
justified text, 1.15 line spacing, plain black bold section headings, clean
tables with a light shaded header row, and numbered figures with captions.
Figures generated by generate_densitograms.py are embedded:
  - HPTLC plate (Fig 4.3 style)
  - 2D densitogram comparison: A = Sample, B = Standard (Fig 4.5 style)
  - 3D densitogram: A Standard + B Sample waterfall (image 1 style)
"""

import os
from docx import Document
from docx.shared import Inches, Pt, Cm, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.enum.table import WD_TABLE_ALIGNMENT
from docx.oxml.ns import qn, nsdecls
from docx.oxml import parse_xml

HERE = os.path.dirname(os.path.abspath(__file__))

BODY_FONT = "Times New Roman"
BODY_SIZE = 11
TABLE_SIZE = 10
HEADER_FILL = "D9D9D9"
BORDER_COLOR = "000000"

QRF = "0.55"   # quercetin retention factor used throughout the document


# --------------------------------------------------------------------------- #
#  Helpers
# --------------------------------------------------------------------------- #
def _set_run_font(run, size=BODY_SIZE, bold=False, italic=False, color=None):
    run.font.name = BODY_FONT
    run.font.size = Pt(size)
    run.bold = bold
    run.italic = italic
    if color is not None:
        run.font.color.rgb = RGBColor(*color)
    rpr = run._element.get_or_add_rPr()
    rfonts = rpr.find(qn('w:rFonts'))
    if rfonts is None:
        rfonts = parse_xml(f'<w:rFonts {nsdecls("w")}/>')
        rpr.append(rfonts)
    rfonts.set(qn('w:ascii'), BODY_FONT)
    rfonts.set(qn('w:hAnsi'), BODY_FONT)


def set_cell_shading(cell, color):
    cell._tc.get_or_add_tcPr().append(
        parse_xml(f'<w:shd {nsdecls("w")} w:fill="{color}"/>'))


def add_table_borders(table, color=BORDER_COLOR):
    tblPr = table._tbl.tblPr
    tblPr.append(parse_xml(
        f'<w:tblBorders {nsdecls("w")}>'
        f'<w:top w:val="single" w:sz="6" w:color="{color}"/>'
        f'<w:left w:val="single" w:sz="6" w:color="{color}"/>'
        f'<w:bottom w:val="single" w:sz="6" w:color="{color}"/>'
        f'<w:right w:val="single" w:sz="6" w:color="{color}"/>'
        f'<w:insideH w:val="single" w:sz="4" w:color="{color}"/>'
        f'<w:insideV w:val="single" w:sz="4" w:color="{color}"/>'
        f'</w:tblBorders>'
    ))


def create_styled_table(doc, headers, rows, bold_first_col=True):
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.style = 'Table Grid'
    add_table_borders(table)

    table._tbl.tblPr.append(parse_xml(
        f'<w:tblCellMar {nsdecls("w")}>'
        f'<w:top w:w="40" w:type="dxa"/><w:left w:w="80" w:type="dxa"/>'
        f'<w:bottom w:w="40" w:type="dxa"/><w:right w:w="80" w:type="dxa"/>'
        f'</w:tblCellMar>'
    ))

    for i, header in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = ""
        p = cell.paragraphs[0]
        p.paragraph_format.space_before = Pt(1)
        p.paragraph_format.space_after = Pt(1)
        p.paragraph_format.line_spacing = 1.0
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run(header)
        _set_run_font(run, size=TABLE_SIZE, bold=True)
        set_cell_shading(cell, HEADER_FILL)

    for r, row_data in enumerate(rows):
        for c, txt in enumerate(row_data):
            cell = table.rows[r + 1].cells[c]
            cell.text = ""
            p = cell.paragraphs[0]
            p.paragraph_format.space_before = Pt(1)
            p.paragraph_format.space_after = Pt(1)
            p.paragraph_format.line_spacing = 1.0
            run = p.add_run(str(txt))
            _set_run_font(run, size=TABLE_SIZE, bold=(bold_first_col and c == 0))
    return table


def add_heading_styled(doc, text, level=1):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(10 if level == 1 else 8)
    p.paragraph_format.space_after = Pt(4)
    p.paragraph_format.keep_with_next = True
    run = p.add_run(text)
    if level == 1:
        _set_run_font(run, size=13, bold=True)
    elif level == 2:
        _set_run_font(run, size=11.5, bold=True)
    else:
        _set_run_font(run, size=11, bold=True, italic=True)
    return p


def add_body_text(doc, text, bold=False, italic=False, size=BODY_SIZE,
                  align="justify"):
    p = doc.add_paragraph()
    if align == "justify":
        p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    elif align == "center":
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_after = Pt(6)
    p.paragraph_format.line_spacing = 1.15
    run = p.add_run(text)
    _set_run_font(run, size=size, bold=bold, italic=italic)
    return p


def add_lead_paragraph(doc, lead, rest):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    p.paragraph_format.space_after = Pt(6)
    p.paragraph_format.line_spacing = 1.15
    r1 = p.add_run(lead)
    _set_run_font(r1, bold=True)
    r2 = p.add_run(rest)
    _set_run_font(r2)
    return p


def add_bullet_points(doc, items):
    for item in items:
        p = doc.add_paragraph(style='List Bullet')
        p.paragraph_format.space_after = Pt(2)
        p.paragraph_format.line_spacing = 1.15
        run = p.add_run(item)
        _set_run_font(run)


def add_figure(doc, filename, caption, width_inches=5.8):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after = Pt(2)
    p.add_run().add_picture(os.path.join(HERE, filename), width=Inches(width_inches))

    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cap.paragraph_format.space_after = Pt(10)
    cap.paragraph_format.line_spacing = 1.0
    parts = caption.split(" ", 2)
    label = " ".join(parts[:2])         # e.g. "Figure 1."
    rest = " " + parts[2] if len(parts) > 2 else ""
    r1 = cap.add_run(label)
    _set_run_font(r1, size=9.5, bold=True)
    r2 = cap.add_run(rest)
    _set_run_font(r2, size=9.5, italic=True)


