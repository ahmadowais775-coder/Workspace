def set_cell_shading(cell, color):
    shading_elm = parse_xml(f'<w:shd {nsdecls("w")} w:fill="{color}"/>')
    cell._tc.get_or_add_tcPr().append(shading_elm)


def add_table_borders(table, color="2E4057"):
    tbl = table._tbl
    tblPr = tbl.tblPr
    borders = parse_xml(
        f'<w:tblBorders {nsdecls("w")}>'
        f'<w:top w:val="single" w:sz="6" w:color="{color}"/>'
        f'<w:left w:val="single" w:sz="6" w:color="{color}"/>'
        f'<w:bottom w:val="single" w:sz="6" w:color="{color}"/>'
        f'<w:right w:val="single" w:sz="6" w:color="{color}"/>'
        f'<w:insideH w:val="single" w:sz="4" w:color="{color}"/>'
        f'<w:insideV w:val="single" w:sz="4" w:color="{color}"/>'
        f'</w:tblBorders>'
    )
    tblPr.append(borders)


def create_styled_table(doc, headers, rows, header_color="1B4F72", widths=None):
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    add_table_borders(table)

    tbl = table._tbl
    tblPr = tbl.tblPr
    cellMargin = parse_xml(
        f'<w:tblCellMar {nsdecls("w")}>'
        f'<w:top w:w="20" w:type="dxa"/>'
        f'<w:left w:w="40" w:type="dxa"/>'
        f'<w:bottom w:w="20" w:type="dxa"/>'
        f'<w:right w:w="40" w:type="dxa"/>'
        f'</w:tblCellMar>'
    )
    tblPr.append(cellMargin)

    for i, header in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = ""
        p = cell.paragraphs[0]
        p.paragraph_format.space_before = Pt(0)
        p.paragraph_format.space_after = Pt(0)
        p.paragraph_format.line_spacing = 1.0
        run = p.add_run(header)
        run.bold = True
        run.font.color.rgb = RGBColor(255, 255, 255)
        run.font.size = Pt(8.5)
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        set_cell_shading(cell, header_color)

    for row_idx, row_data in enumerate(rows):
        for col_idx, cell_text in enumerate(row_data):
            cell = table.rows[row_idx + 1].cells[col_idx]
            cell.text = ""
            p = cell.paragraphs[0]
            p.paragraph_format.space_before = Pt(0)
            p.paragraph_format.space_after = Pt(0)
            p.paragraph_format.line_spacing = 1.0
            run = p.add_run(str(cell_text))
            run.font.size = Pt(8)
            if col_idx == 0:
                run.bold = True
            if row_idx % 2 == 0:
                set_cell_shading(cell, "EBF5FB")
    return table


def add_highlight_box(doc, title, content, color="FEF9E7", border_color="F39C12",
                      title_color=(0x8B, 0x45, 0x13)):
    table = doc.add_table(rows=1, cols=1)
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    cell = table.rows[0].cells[0]
    set_cell_shading(cell, color)

    tcPr = cell._tc.get_or_add_tcPr()
    tcPr.append(parse_xml(
        f'<w:tcMar {nsdecls("w")}>'
        f'<w:top w:w="30" w:type="dxa"/><w:left w:w="60" w:type="dxa"/>'
        f'<w:bottom w:w="30" w:type="dxa"/><w:right w:w="60" w:type="dxa"/>'
        f'</w:tcMar>'
    ))
    tcPr.append(parse_xml(
        f'<w:tcBorders {nsdecls("w")}>'
        f'<w:top w:val="single" w:sz="12" w:color="{border_color}"/>'
        f'<w:bottom w:val="single" w:sz="12" w:color="{border_color}"/>'
        f'<w:left w:val="single" w:sz="12" w:color="{border_color}"/>'
        f'<w:right w:val="single" w:sz="12" w:color="{border_color}"/>'
        f'</w:tcBorders>'
    ))

    cell.text = ""
    p = cell.paragraphs[0]
    p.paragraph_format.space_before = Pt(0)
    p.paragraph_format.space_after = Pt(1)
    p.paragraph_format.line_spacing = 1.0
    run = p.add_run(f"  {title}")
    run.bold = True
    run.font.size = Pt(9.5)
    run.font.color.rgb = RGBColor(*title_color)

    items = content if isinstance(content, list) else [content]
    for item in items:
        p = cell.add_paragraph()
        p.paragraph_format.space_before = Pt(0)
        p.paragraph_format.space_after = Pt(0)
        p.paragraph_format.line_spacing = 1.0
        run = p.add_run(f"  {item}")
        run.font.size = Pt(8.5)


def add_section_divider(doc, color="1B4F72"):
    table = doc.add_table(rows=1, cols=1)
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    cell = table.rows[0].cells[0]
    cell.text = ""
    p = cell.paragraphs[0]
    p.paragraph_format.space_before = Pt(0)
    p.paragraph_format.space_after = Pt(0)
    tcPr = cell._tc.get_or_add_tcPr()
    tcPr.append(parse_xml(
        f'<w:tcBorders {nsdecls("w")}>'
        f'<w:bottom w:val="single" w:sz="18" w:color="{color}"/>'
        f'</w:tcBorders>'
    ))


def add_heading_styled(doc, text, level=1):
    heading = doc.add_heading(text, level=level)
    for run in heading.runs:
        if level == 1:
            run.font.color.rgb = RGBColor(0x1B, 0x4F, 0x72)
            run.font.size = Pt(16)
        elif level == 2:
            run.font.color.rgb = RGBColor(0x15, 0x4F, 0x0B)
            run.font.size = Pt(13)
        elif level == 3:
            run.font.color.rgb = RGBColor(0x6C, 0x3A, 0x83)
            run.font.size = Pt(11)
    return heading


def add_bullet_points(doc, items, bold_prefix=False):
    for item in items:
        p = doc.add_paragraph(style='List Bullet')
        if bold_prefix and ':' in item:
            parts = item.split(':', 1)
            run = p.add_run(parts[0] + ':')
            run.bold = True
            run.font.size = Pt(9)
            run = p.add_run(parts[1])
            run.font.size = Pt(9)
        else:
            run = p.add_run(item)
            run.font.size = Pt(9)


def add_body_text(doc, text, bold=False, italic=False, size=9.5):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.bold = bold
    run.italic = italic
    return p


def add_figure(doc, filename, caption, width_inches=6.6):
    path = os.path.join(HERE, filename)
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_before = Pt(3)
    p.paragraph_format.space_after = Pt(1)
    run = p.add_run()
    run.add_picture(path, width=Inches(width_inches))

    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cap.paragraph_format.space_before = Pt(0)
    cap.paragraph_format.space_after = Pt(4)
    run = cap.add_run(caption)
    run.italic = True
    run.bold = True
    run.font.size = Pt(8.5)
    run.font.color.rgb = RGBColor(0x56, 0x6D, 0x7E)


