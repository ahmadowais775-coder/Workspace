            ["Migration distance", "80 mm from the application position"],
            ["Band length / spacing", "8 mm band; 6 bands per plate; 15 mm from lower edge"],
            ["Application volume", "Standard 4 \u00b5L; EA fraction 8 \u00b5L (10 mg/mL)"],
            ["Development conditions", "25 \u00b1 2 \u00b0C; relative humidity 50 \u00b1 5%"],
            ["Detection wavelengths", "254 nm (quenching); 366 nm (fluorescence after NP\u2013PEG)"],
            ["Scanning mode", "Absorption/reflectance; slit 5.00 \u00d7 0.45 mm; 20 mm/s"],
            ["Lamp", "Deuterium and tungsten (D2 / W)"],
