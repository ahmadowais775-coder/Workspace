def add_discussion(doc):
    add_heading_styled(doc, "8. Discussion", 1)
    add_body_text(doc,
        "The HPTLC fingerprint of the ethyl acetate fraction of the S. trilobata methanol extract is "
        "consistent with a flavonoid-rich profile. The comparison\u2014across the developed plate, the "
        "2D densitometric profiles, and the 3D overlay\u2014established an unambiguous correspondence "
        "between one of the native bands of the fraction and the quercetin reference standard. The "
        "aglycone quercetin migrated as a single, well-resolved band at Rf " + QRF + " under the "
        "normal-phase silica gel system, and the corresponding band in the sample matched it in Rf, "
        "fluorescence colour, and UV spectrum.")
    add_body_text(doc,
        "The observation that quercetin (peak P5, ~36.6% area) was the dominant band of the EA "
        "fraction suggests that the ethyl acetate fractionation step is highly effective for enriching "
        "this therapeutically relevant flavonoid. The presence of quercetin may rationalise the "
        "reported antioxidant, anti-inflammatory, and hepatoprotective activities of the plant, since "
        "quercetin is an established free-radical scavenger.")


def add_conclusion(doc):
    add_heading_styled(doc, "9. Conclusion", 1)
    add_body_text(doc,
        "A simple, precise, and specific HPTLC method was developed and applied to fingerprint the "
        "ethyl acetate fraction of the methanol extract of Sphagneticola trilobata (Wedelia "
        "trilobata). The HPTLC plate, the 2D densitograms, and the 3D densitogram, supported by Rf "
        "matching, co-chromatography, and spectral overlay, conclusively confirmed the presence of "
        "the flavonoid quercetin (Rf " + QRF + ") as a major constituent of the fraction. The method "
        "is reproducible and may be adopted as a quality-control fingerprint and for the marker-based "
        "standardisation of S. trilobata preparations.")


