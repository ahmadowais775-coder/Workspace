        "reagent and examined at 366 nm. The developed plate (Figure 1) shows the bands of the "
        "quercetin standard and of the ethyl acetate fraction. The band of the EA fraction that lies "
        "at the same height (Rf " + QRF + ") as the quercetin standard confirms, on visual inspection, "
